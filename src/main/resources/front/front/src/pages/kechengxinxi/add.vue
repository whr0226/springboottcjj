<script>
  export default {
    data() {
      return {
        id: '',
        type: '',
        userTableName: localStorage.getItem('UserTableName'),
        ruleForm: {
          kechengmingcheng: '',
          kechengtupian: '',
          kechengleibie: '',
          shangkeshijian: '',
          keshijiage: '',
          gongzuozhuangtai: '',
          jiaoshigonghao: '',
          jiaoshixingming: '',
          xingbie: '',
          xueli: '',
          lianxidianhua: '',
        },
        kechengleibieOptions: [],
        gongzuozhuangtaiOptions: [],
        jiaoshigonghaoOptions: [],
        rules: {
          kechengmingcheng: [
          ],
          kechengtupian: [
          ],
          kechengleibie: [
          ],
          shangkeshijian: [
          ],
          keshijiage: [
            { validator: this.$validate.isIntNumer, trigger: 'blur' },
          ],
          gongzuozhuangtai: [
          ],
          jiaoshigonghao: [
          ],
          jiaoshixingming: [
          ],
          xingbie: [
          ],
          xueli: [
            { required: true, message: '学历不能为空', trigger: 'blur' },
          ],
          lianxidianhua: [
            { required: true, message: '联系电话不能为空', trigger: 'blur' },
            { validator: this.$validate.isMobile, trigger: 'blur' },
          ],
        },
      };
    },
    computed: {



    },
    created() {
	  //this.bg();
      let type = this.$route.query.type ? this.$route.query.type : '';
      this.init(type);
    },
    methods: {
      getMakeZero(s) {
          return s < 10 ? '0' + s : s;
      },
      // 下载
      download(file){
        window.open(`${file}`)
      },
      // 初始化
      init(type) {
        this.type = type;
        if(type=='cross'){
          var obj = JSON.parse(localStorage.getItem('crossObj'));
          for (var o in obj){
            if(o=='kechengmingcheng'){
              this.ruleForm.kechengmingcheng = obj[o];
              continue;
            }
            if(o=='kechengtupian'){
              this.ruleForm.kechengtupian = obj[o].split(",")[0];
              continue;
            }
            if(o=='kechengleibie'){
              this.ruleForm.kechengleibie = obj[o];
              continue;
            }
            if(o=='shangkeshijian'){
              this.ruleForm.shangkeshijian = obj[o];
              continue;
            }
            if(o=='keshijiage'){
              this.ruleForm.keshijiage = obj[o];
              continue;
            }
            if(o=='gongzuozhuangtai'){
              this.ruleForm.gongzuozhuangtai = obj[o];
              continue;
            }
            if(o=='jiaoshigonghao'){
              this.ruleForm.jiaoshigonghao = obj[o];
              continue;
            }
            if(o=='jiaoshixingming'){
              this.ruleForm.jiaoshixingming = obj[o];
              continue;
            }
            if(o=='xingbie'){
              this.ruleForm.xingbie = obj[o];
              continue;
            }
            if(o=='xueli'){
              this.ruleForm.xueli = obj[o];
              continue;
            }
            if(o=='lianxidianhua'){
              this.ruleForm.lianxidianhua = obj[o];
              continue;
            }
          }
        }
        // 获取用户信息
        this.$http.get(this.userTableName + '/session', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            var json = res.data.data;
          }
        });
        this.$http.get('option/kechengleibie/kechengleibie', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            this.kechengleibieOptions = res.data.data;
          }
        });
        this.gongzuozhuangtaiOptions = "已预约,可预约".split(',')
        this.$http.get('option/jiajiaolaoshi/jiaoshigonghao', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            this.jiaoshigonghaoOptions = res.data.data;
          }
        });
      },
      // 下二随
      jiaoshigonghaoChange () {
        this.$http.get('follow/jiajiaolaoshi/jiaoshigonghao?columnValue=' + this.ruleForm.jiaoshigonghao, {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            if(res.data.data.jiaoshixingming){
              this.ruleForm.jiaoshixingming = res.data.data.jiaoshixingming
            }
            if(res.data.data.xingbie){
              this.ruleForm.xingbie = res.data.data.xingbie
            }
            if(res.data.data.xueli){
              this.ruleForm.xueli = res.data.data.xueli
            }
            if(res.data.data.lianxidianhua){
              this.ruleForm.lianxidianhua = res.data.data.lianxidianhua
            }
          }
        });
      },

    // 多级联动参数
      // 多级联动参数
      info(id) {
        this.$http.get('kechengxinxi/detail/${id}', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            this.ruleForm = res.data.data;
          }
        });
      },
      // 提交
      onSubmit() {

        //更新跨表属性
        var crossuserid;
        var crossrefid;
        var crossoptnum;
        this.$refs["ruleForm"].validate(valid => {
          if(valid) {
            if(this.type=='cross'){
                 var statusColumnName = localStorage.getItem('statusColumnName');
                 var statusColumnValue = localStorage.getItem('statusColumnValue');
                 if(statusColumnName && statusColumnName!='') {
                     var obj = JSON.parse(localStorage.getItem('crossObj'));
                     if(!statusColumnName.startsWith("[")) {
                         for (var o in obj){
                             if(o==statusColumnName){
                                 obj[o] = statusColumnValue;
                             }
                         }
                         var table = localStorage.getItem('crossTable');
                         this.$http.post(table+'/update', obj).then(res => {});
                     } else {
                            crossuserid=Number(localStorage.getItem('userid'));
                            crossrefid=obj['id'];
                            crossoptnum=localStorage.getItem('statusColumnName');
                            crossoptnum=crossoptnum.replace(/\[/,"").replace(/\]/,"");
                     }
                 }
            }
            if(crossrefid && crossuserid) {
                 this.ruleForm.crossuserid=crossuserid;
                 this.ruleForm.crossrefid=crossrefid;
                 var params = {
                     page: 1,
                     limit: 10,
                     crossuserid:crossuserid,
                     crossrefid:crossrefid,
                 }
                 this.$http.get('kechengxinxi/list', {
                  params: params
                 }).then(res => {
                     if(res.data.data.total>=crossoptnum) {
                         this.$message({
                          message: localStorage.getItem('tips'),
                          type: 'success',
                          duration: 1500,
                         });
                          return false;
                     } else {
                         // 跨表计算


                          this.$http.post('kechengxinxi/add', this.ruleForm).then(res => {
                              if (res.data.code == 0) {
                                  this.$message({
                                      message: '操作成功',
                                      type: 'success',
                                      duration: 1500,
                                      onClose: () => {
                                          this.$router.go(-1);
                                      }
                                  });
                              } else {
                                  this.$message({
                                      message: res.data.msg,
                                      type: 'error',
                                      duration: 1500
                                  });
                              }
                          });
                     }
                 });
             } else {


                  this.$http.post('kechengxinxi/add', this.ruleForm).then(res => {
                     if (res.data.code == 0) {
                          this.$message({
                              message: '操作成功',
                              type: 'success',
                              duration: 1500,
                              onClose: () => {
                                  this.$router.go(-1);
                              }
                          });
                      } else {
                          this.$message({
                              message: res.data.msg,
                              type: 'error',
                              duration: 1500
                          });
                      }
                  });
             }
          }
        });
      },
      // 获取uuid
      getUUID () {
        return new Date().getTime();
      },
      // 返回
      back() {
        this.$router.go(-1);
      },
      kechengtupianUploadChange(fileUrls) {
          this.ruleForm.kechengtupian = fileUrls.replace(new RegExp(this.$config.baseUrl,"g"),"");;
      },
    }
  };
</script>

