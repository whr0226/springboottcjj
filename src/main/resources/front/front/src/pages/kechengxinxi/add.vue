            ></file-upload>
          </el-form-item>
          <el-form-item :style='{"width":"100%","padding":"10px","margin":"0 0 0px","display":"inline-block"}'  label="课程类别" prop="kechengleibie">
            <el-select v-model="ruleForm.kechengleibie" placeholder="请选择课程类别" >
              <el-option
                  v-for="(item,index) in kechengleibieOptions"
                  :key="index"
                  :label="item"
                  :value="item">
              </el-option>
            </el-select>
          </el-form-item>
          <el-form-item :style='{"width":"100%","padding":"10px","margin":"0 0 0px","display":"inline-block"}' label="上课时间" prop="shangkeshijian">
            <el-input v-model="ruleForm.shangkeshijian" 
                placeholder="上课时间" clearable ></el-input>
          </el-form-item>
          <el-form-item :style='{"width":"100%","padding":"10px","margin":"0 0 0px","display":"inline-block"}' label="课时价格" prop="keshijiage">
            <el-input v-model="ruleForm.keshijiage" 
                placeholder="课时价格" clearable readonly></el-input>
          </el-form-item>
          <el-form-item :style='{"width":"100%","padding":"10px","margin":"0 0 0px","display":"inline-block"}'  label="工作状态" prop="gongzuozhuangtai">
            <el-select v-model="ruleForm.gongzuozhuangtai" placeholder="请选择工作状态" >
              <el-option
                  v-for="(item,index) in gongzuozhuangtaiOptions"
                  :key="index"
                  :label="item"
                  :value="item">
              </el-option>
            </el-select>
          </el-form-item>
          <el-form-item :style='{"width":"100%","padding":"10px","margin":"0 0 0px","display":"inline-block"}' label="教师工号" prop="jiaoshigonghao">
            <el-select  @change="jiaoshigonghaoChange" v-model="ruleForm.jiaoshigonghao" placeholder="请选择教师工号">
              <el-option
                  v-for="(item,index) in jiaoshigonghaoOptions"
                  :key="index"
                  :label="item"
