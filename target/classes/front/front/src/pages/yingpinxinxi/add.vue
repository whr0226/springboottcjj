            :fileUrls="ruleForm.gerenjianli?ruleForm.gerenjianli:''"
            @change="gerenjianliUploadChange"
            ></file-upload>
          </el-form-item>  
          <el-form-item :style='{"width":"100%","padding":"10px","margin":"0 0 0px","display":"inline-block"}' label="应聘留言" prop="yingpinliuyan">
            <el-input v-model="ruleForm.yingpinliuyan" 
                placeholder="应聘留言" clearable ></el-input>
          </el-form-item>
          <el-form-item :style='{"width":"100%","padding":"10px","margin":"0 0 0px","display":"inline-block"}' label="教师工号" prop="jiaoshigonghao">
